; ----------------------------------------------------------------------------
; i64 factorial(i64 n) 
;   [ x0 ] = n
; ----------------------------------------------------------------------------
factorial:
    mov x1, 1
    mov x2, x0
facst:
    cmp   x1,x2
    jge   facen
    mul   x0,x1
    inc   x1
    jmp   facst
facen:
    ret

; ----------------------------------------------------------------------------
; i64 main() 
; ----------------------------------------------------------------------------
main:
    mov x0, 12
    call factorial
    prg x0
    mov x0, 0
    ret
